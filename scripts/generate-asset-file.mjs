import fs              from 'fs';
import path            from 'path';
import {fileURLToPath} from 'url';

/**
 * The `__filename` variable provides the absolute file path of the current
 * module in which it is used. This value is derived from the `import.meta.url`,
 * which is a URL string representing the location of the file. It is converted
 * into a file path using the `fileURLToPath` utility from the `url` module.
 *
 * This variable is commonly used to reference the current file's location
 * in file system operations or to resolve paths relative to the script.
 *
 * Note: This variable is specific to ES modules and serves a similar purpose
 * to the `__filename` global available in CommonJS modules.
 */
const __filename = fileURLToPath(import.meta.url);
/**
 * @variable __dirname
 * @description A global variable that contains the directory path of the currently executing script.
 *              It is equivalent to the directory name of the file where the script is located.
 *              Useful for constructing paths relative to the script's location.
 * @type {string}
 */
const __dirname = path.dirname(__filename);

/**
 * Represents the parsed content of the package.json file.
 * Contains metadata and configuration information for the Node.js project,
 * including details such as name, version, dependencies, scripts, and more.
 *
 * This variable is initialized by reading the package.json file located
 * in the parent directory of the current module. The file is read as a
 * UTF-8 encoded string and then parsed into a JavaScript object.
 *
 * Note: Accessing properties of this variable should be done with care
 * as modifications may affect the core configuration of the project.
 *
 * It is primarily used for reading project metadata, managing dependencies,
 * and automating various tasks defined in the scripts section of package.json.
 */
const packageJson = JSON.parse(
    fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8')
);

/**
 * Represents the content of an asset file in PHP format, typically used for WordPress integration.
 *
 * The content contains a PHP array definition which includes metadata such as dependencies and version
 * information. This is often generated by Vite for use in WordPress projects.
 *
 * The `dependencies` key specifies required assets or libraries, and the `version` key is dynamically
 * set based on the version in the associated package.json file.
 */
const assetFileContent = `<?php
/**
 * Generated by Vite
 *
 * @return array Asset data for WordPress
 */
return array(
	'dependencies' => array(),
	'version'      => '${packageJson.version}',
);`;

/**
 * Represents the directory where the compiled or generated JavaScript assets are stored.
 * This path is constructed relative to the current file's directory.
 * Typically used to specify the target location for JavaScript output files.
 */
const outputDir = path.join(__dirname, '..', 'assets', 'js');
/**
 * A string representing the full file path to the output asset file.
 * This variable is constructed by joining the directory path specified in `outputDir`
 * with the file name `index.asset.php`.
 *
 * The generated file typically serves as a PHP asset manifest containing information
 * about dependencies, versioning, or other metadata required for the application.
 *
 * @type {string}
 */
const outputFile = path.join(outputDir, 'index.asset.php');

// Ensure the directory exists
if (!fs.existsSync(outputDir)) {
  fs.mkdirSync(outputDir, {recursive: true});
}

// Write the asset file
fs.writeFileSync(outputFile, assetFileContent, 'utf8');

console.log('✓ Generated index.asset.php');
